package lib.uilib.framework.enums;

/**
 * Enum representing the cross axis alignment of a widget.
 * 
 * @author Bryan Soong, Joyce Lee
 * @version 1.0
 * @since 2024-11-10
 */
public enum CrossAxisAlignment {
    /**
     * Aligns the widget to the center.
     */
    CENTER,

    /**
     * Aligns the widget to the start.
     */
    START,

    /**
     * Aligns the widget to the end.
     */
    END,

    /**
     * Aligns the widget to the space between.
     */
    SPACE_BETWEEN,

    /**
     * Aligns the widget to the space around.
     */
    SPACE_AROUND,
}
